#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*pt;
	size_t	i;

	i = 0;
	pt = (char *)s;
	while (i < n)
	{
		pt[i] = (char)c;
		i++;
	}
	return (s);
}
